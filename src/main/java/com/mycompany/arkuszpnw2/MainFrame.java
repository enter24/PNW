    private void setReqiureSocial(){
        //=ZAOKR.W.GÓRĘ(H4/4;0,1;1)*(1+K12)
        double value = Double.valueOf(tRiches.getText());
        double value2 = (int) sBonusPeopleReqiure1.getValue();
        double value3 = (int) sBonusPeopleReqiure2.getValue();
        value = value/4*(1+value2/100+value3/100)*10;
        value=Math.ceil(value);
        tSocialReqiure.setText(String.valueOf(value/10));
    }
    //eksport
    private void setExport(){
        if(Integer.valueOf(tProductionGood.getText())>=Integer.valueOf(tRequireedGood.getText())){
            if((Integer.valueOf(tProductionGood.getText())-Integer.valueOf(tRequireedGood.getText()))>((int)sTradeLevel.getValue()*5)){
                lEksport.setText(String.valueOf(Math.rint((int)sTradeLevel.getValue()*5)));
            }else{
                lEksport.setText(String.valueOf(Math.rint(Integer.valueOf(tProductionGood.getText())-Integer.valueOf(tRequireedGood.getText()))));
            }
        }else{
            int value = Integer.valueOf(tRequireedGood.getText())-Integer.valueOf(tProductionGood.getText());
            lEksport.setText(String.valueOf(Math.rint((double)value*((int)sTradeLevel.getValue()*0.05-1.55))));
        }
    }
