    private void setPeopleloss(){
        //=JEŻELI(H18+H19<=0,2*H20;0;ZAOKR.W.GÓRĘ((H18+H19)/H20-0,2;0,1;1))
        double value = Double.parseDouble(lPeoplePowerAll.getText())*0.2;
        int value2 = Integer.valueOf(lPeoplePowerProduction.getText())+(int)sPeoplePowerUsed.getValue();
        if(value2>value){
            lPeoplePowerLoss.setText("0");
        }else{
            double value3 = ((int)sPeoplePowerUsed.getValue()+Integer.valueOf(lPeoplePowerProduction.getText())) / (Double.parseDouble(lPeoplePowerAll.getText())-0.2);
            //double value5 = (double) value4/value3-0.2)*10;
            value3 = Math.ceil(value3);
            lPeoplePowerLoss.setText(String.valueOf(value3));
        }
    }
