    private void setRequireedGood(){
        double value = Double.valueOf(tRiches.getText());
        double value3 = (int)sProwControl.getValue()/(int)sProwAll.getValue();
        int value2 = (int)value+(int)sTradeLevel.getValue();
        value = Double.valueOf(tPeople.getText())*value3*(value2*0.05+0.95);
        value = Math.rint(value);
        tRequireedGood.setText(String.valueOf((int)value));     
       
    }
